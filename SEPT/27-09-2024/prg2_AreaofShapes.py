PI = 3.14
radius = int(input("Enter radius of your circle"))
print(f'Area of Circle is {radius*radius*PI}')
length = int(input("Enter length of your Rectangle"))
breadth = int(input("Enter breadth of your Rectangle"))
print(f'Area of Rectangle is {length * breadth}')
base = int(input("Enter base of your triangle"))
height_triangle = int(input("Enter height of your triangle"))
print(f'Area of Triangle is {(base*height_triangle)/2}')
